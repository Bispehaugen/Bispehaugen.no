    function changeHash(toHash) {
        if (href) {
            if (history.pushState) {
                history.pushState(null, null, href);
            } else {
                var scrollTopBeforeHashChange = $('body').scrollTop();
                window.location.hash = toHash;
                $('html,body').scrollTop(scrollTopBeforeHashChange);
            }
        }
    }

